// Plural forms: msgid (singular) => [ msgstr[0] (en singular), msgstr[1] (en plural) ].
$plural_translations = [
    'Użytkownik dodany do %d grupy.' => [ 'User added to %d group.', 'User added to %d groups.' ],
    'Usunięto %d głosowanie.' => [ '%d poll deleted.', '%d polls deleted.' ],
    'Zakończono %d głosowanie.' => [ '%d poll ended.', '%d polls ended.' ],
];

$pot = file_get_contents( $pot_path );
if ( $pot === false ) {
    fwrite( STDERR, "Cannot read $pot_path\n" );
    exit( 1 );
}

$en_header = '"Project-Id-Version: Open Vote\n"
"Report-Msgid-Bugs-To: \n"
"POT-Creation-Date: ' . date( 'Y-m-d H:iO' ) . '\n"
"PO-Revision-Date: ' . date( 'Y-m-d H:iO' ) . '\n"
"Last-Translator: \n"
"Language-Team: English\n"
"Language: en_US\n"
"MIME-Version: 1.0\n"
"Content-Type: text/plain; charset=UTF-8\n"
"Content-Transfer-Encoding: 8bit\n"
"Plural-Forms: nplurals=1; plural=0;\n"
"X-Generator: make-en-us-po.php\n"
';

// Replace the header (first msgstr block).
$pot = preg_replace(
    '/^msgstr ""\n""[^"]*"Plural-Forms:[^"]*";\n"/m',
    'msgstr ""' . "\n" . $en_header . '"',
    $pot,
    1
);

// For each "msgid \"...\"\nmsgstr \"\"" replace msgstr with translation if we have it.
$lines = explode( "\n", $pot );
$out = [];
$i = 0;
$n = count( $lines );
while ( $i < $n ) {
    $line = $lines[ $i ];
    $out[] = $line;
    // Match line that is msgid "something" (possibly multiline)
    if ( preg_match( '/^msgid "(.*)"\s*$/', $line, $m ) ) {
        $msgid = str_replace( '\\n', "\n", $m[1] );
        $j = $i + 1;
        while ( $j < $n && preg_match( '/^"(.*)"\s*$/', $lines[ $j ] ) ) {
            $msgid .= str_replace( '\\n', "\n", substr( $lines[ $j ], 1, -1 ) );
            $out[] = $lines[ $j ];
            $j++;
        }
        $i = $j - 1;
        // Next non-empty line should be msgstr or msgid_plural
        if ( $i + 1 < $n ) {
            $next = $lines[ $i + 1 ];
            if ( preg_match( '/^msgstr "(.*)"\s*$/', $next ) && trim( $next ) !== 'msgstr ""' ) {
                $i++;
                $out[] = $next;
                $i++;
                continue;
            }
            if ( preg_match( '/^msgstr ""\s*$/', $next ) && $msgid !== '' && isset( $translations[ $msgid ] ) ) {
                $i++;
                $trans = $translations[ $msgid ];
                $trans = addcslashes( $trans, '"\\' );
                $trans = str_replace( "\n", '\\n"' . "\n" . '"', $trans );
                $out[] = 'msgstr "' . $trans . '"';
                $i++;
                continue;
            }
            if ( preg_match( '/^msgstr ""\s*$/', $next ) ) {
                $i++;
                $out[] = $next;
                $i++;
                continue;
            }
            // Plural: msgid_plural, then msgstr[0] "" and msgstr[1] ""
            if ( preg_match( '/^msgid_plural "(.*)"\s*$/', $next ) && $msgid !== '' && isset( $plural_translations[ $msgid ] ) ) {
                $pl = $plural_translations[ $msgid ];
                $i++;
                $out[] = $next;
                $i++;
                // Skip possible continuation lines for msgid_plural (multiline)
                while ( $i < $n && preg_match( '/^"(.*)"\s*$/', $lines[ $i ] ) ) {
                    $out[] = $lines[ $i ];
                    $i++;
                }
                // Next: msgstr[0] ""
                if ( $i < $n && preg_match( '/^msgstr\[0\] "(.*)"\s*$/', $lines[ $i ], $m0 ) ) {
                    $out[] = 'msgstr[0] "' . addcslashes( $pl[0], '"\\' ) . '"';
                    $i++;
                }
                if ( $i < $n && preg_match( '/^msgstr\[1\] "(.*)"\s*$/', $lines[ $i ], $m1 ) ) {
                    $out[] = 'msgstr[1] "' . addcslashes( $pl[1], '"\\' ) . '"';
                    $i++;
                }
                continue;
            }
        }
    }
    $i++;
}

$po_content = implode( "\n", $out );

// Plural forms: leave msgstr[0]/msgstr[1] as in .pot; fix manually in .po if needed.
file_put_contents( $po_path, $po_content );
echo "Written $po_path\n";
echo "Run: msgfmt -o " . __DIR__ . "/openvote-en_US.mo $po_path\n";
